# 列表、元组、字典和集合的区别

# 数据类型     序列类型       是否重复       是否有序     定义称号
# 列表list     可变序列        可重复         有序          []
# 元组tuple    不可变序列      可重复         有序          ()
# 字典dict     可变序列       key不可重复     无序       {key:value} 
#                             value可重复
# 集合set      可变序列        不可重复        无序         {}