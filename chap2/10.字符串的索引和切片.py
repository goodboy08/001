# 语法结构：
# 字符串或字符串变量[序号]

s='HELLOWORLD'
print(s[0],s[-10])  # 序号0和序号-10,表示的为同一个位置

print('北京欢迎你'[4])  # 正向递增序号：从0开始
print('北京欢迎你'[-1])  # 反向递减序号：从-1开始

print('--------------')
# 语法结构：
# 字符串或字符串变量[N:M]
print(s[2:7])    # 正向从0-7,不包含7
print(s[-8:-3])  # 反向从-8到-3,不包含-3
# N默认从0开始
print(s[:5])
# M默认是切到字符串的结尾
print(s[5:])

print(s)  # 默认从0到结尾